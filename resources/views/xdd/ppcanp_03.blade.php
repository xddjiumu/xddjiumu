    <h2 class="titl">PE-RT地暖管</h2>
    　　PE-RT家装采暖管，是一种可以用于热水管的非交联的聚乙烯，也有人突出了其非交联的特性，叫它&quot;耐高温非交联聚乙烯&quot;。它是一种采用特殊的分子设计和合成工艺生产的一种中密度聚乙烯，它采用乙烯和辛烯共聚的方法，通过控制侧链的数量和分布得到独特的分子结构，来提高PE的耐热性。由于辛烯短支链的存在使PE的大分子不能结晶在一个片状晶体中，而是贯穿在几个晶体中，形成了晶体之间的联结，它保留了PE的良好的柔韧性，高热传导性和惰性，同时使之耐压性更好，这种改性方法和目前市场上常见的PPR类似。PE-RT管可以用于ISO10508中规定的热水管的所有使用级别! <br/><br/><img src="/xdd/images/p3.jpg" alt=""/><br/><br/>
