    <h2 class="titl">PE-RT阻氧管    </h2>
                　　PE-RT阻氧地暖管，主要用于地面辐射采暖系统，采用优质的阻氧层EVOH系列中的FP104B材料生产，其PE-RT阻氧管的透氧率是0.07，            德国DIN4726及国家标准GB28799关于带阻氧层的PE-RT的要求为0.1，完全满足标准要求。三层阻氧型PERT采用陶氏化学2388II料，            三层阻氧有效阻止氧气渗透，对系统末端零部件起到良好保护作用，避免腐蚀老化，延长其使用寿命。<br/><br/>
